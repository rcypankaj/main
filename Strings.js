/* Strings in JavaScript */

// Start

// length of the string
let name = 'Pankaj Yadav';
console.log(name.length);

console.log(name[0]);
console.log(name[1]);

// Template Literal
let friend1 = "Pankaj";
let friend2 = "Yadav";
let sentence = `${friend1} and ${friend2} my friend`;
console.log(sentence);

// Escape Sequence Character
name = "S\tuj\"a\'l";
console.log(name);

