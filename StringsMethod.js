/* Strings Method in JavaScript */

// Start

// length of the string
let name = '   Pankaj Yadav';
console.log(name.length);

// for uppercase
console.log(name.toUpperCase());

// for LowerCase
console.log(name.toLowerCase());

// for Slice
console.log(name.slice(0,6));

// for replace
console.log(name.replace("Yadav", "Kumar"));

// for concatenate
console.log(name.slice(0,6).concat(" Kumar Yadav"));

console.log(name);
// for space remove
console.log(name.trim());


